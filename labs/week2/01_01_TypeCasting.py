# Type Casting Exercise

a = 7
print("Type of var a is : "  + f"{type(a)}")

flt_a = float(a)


print("Type of var flt_a from a is : "  + f"{flt_a}")

str_1= str(flt_a)
print("Type of var str_1 from flt_a is : "  + f"{type(str_1)}")

int_1 = float(str_1)
int_2 =  int(int_1)
print("Type of var int_1 from str_1 is : "  + f"{type(int_2)}")

